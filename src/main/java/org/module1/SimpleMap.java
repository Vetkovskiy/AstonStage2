package org.module1;

/**
 * Интерфейс SimpleMap описывает контракт для структуры данных "словарь".
 *
 * @param <K> тип ключей
 * @param <V> тип значений
 */
public interface SimpleMap<K, V> {

    /**
     * Добавляет или обновляет значение по ключу.
     *
     * @param key ключ
     * @param value значение
     * @return замененное значение или null, если значение отсутствовало
     */
    V put(K key, V value);

    /**
     * Возвращает значение по ключу.
     *
     * @param key ключ
     * @return значение или null, если ключ не найден
     */
    V get(K key);

    /**
     * Удаляет значение по ключу.
     *
     * @param key ключ
     * @return удалённое значение или null, если ключ не найден
     */
    V remove(K key);

    /**
     * Возвращает количество элементов в словаре.
     *
     * @return размер словаря
     */
    int size();

    /**
     * Проверяет, содержит ли словарь заданный ключ.
     *
     * @param key ключ
     * @return true, если ключ присутствует
     */
    boolean containsKey(K key);

    /**
     * Проверяет, пуст ли словарь.
     *
     * @return true, если нет ни одного элемента
     */
    boolean isEmpty();

}
